import math

# Constants
print("PI:", math.pi)
print("Euler's number (e):", math.e)

# Basic mathematical functions
print("Square root of 16:", math.sqrt(16))
print("2 raised to the power of 3:", math.pow(2, 3))
print("Absolute value of -5:", abs(-5))
print("Rounded value of 3.7:", round(3.7))

# Trigonometric functions
print("Sine of 90 degrees:", math.sin(math.radians(90)))
print("Cosine of pi:", math.cos(math.pi))

# Hyperbolic functions
print("Hyperbolic sine of 1:", math.sinh(1))
print("Hyperbolic cosine of 1:", math.cosh(1))

# Logarithmic functions
print("Natural logarithm of 10:", math.log(10))
print("Base-10 logarithm of 100:", math.log10(100))
print("Base-10 logarithm of 100:", math.log2(8))

# Exponential functions
print("Exponential of 2:", math.exp(2))
